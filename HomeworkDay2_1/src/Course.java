
public class Course {
	public Course(int id, String name, String detail, String instructor, int progression) {
		
		this.id = id;
		this.name = name;
		this.detail = detail;
		this.instructor = instructor;
		this.progression = progression;
	}
	
	int id;
	String name;
	String detail;
	String instructor;
	int progression;
}
